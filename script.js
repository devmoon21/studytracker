        function generateSegmentedCircleHTML(percentage, size = 80) {
            let activeColor = "#71717a";
            if (percentage > 0 && percentage <= 10) activeColor = "#ef4444";
            else if (percentage > 10 && percentage <= 30) activeColor = "#f97316";
            else if (percentage > 30 && percentage <= 50) activeColor = "#eab308";
            else if (percentage > 50 && percentage <= 70) activeColor = "#22c55e";
            else if (percentage > 70 && percentage <= 90) activeColor = "#3b82f6";
            else if (percentage > 90) activeColor = "#15803d";

            const isDark = document.documentElement.getAttribute('data-theme') === 'dark';
            const trackColor = isDark ? "#334155" : "#e2e8f0";

            let svgHtml = `<svg width="${size}" height="${size}" viewBox="0 0 60 60" style="transform: rotate(-90deg); display: block;">`;
            for (let i = 0; i < 10; i++) {
                const startAngle = (i * 36) * Math.PI / 180;
                const endAngle = ((i + 1) * 36 - 5) * Math.PI / 180;
                const r = 24;
                const x1 = 30 + r * Math.cos(startAngle);
                const y1 = 30 + r * Math.sin(startAngle);
                const x2 = 30 + r * Math.cos(endAngle);
                const y2 = 30 + r * Math.sin(endAngle);
                const segmentValueThreshold = (i + 1) * 10;
                const isSegmentLit = (percentage >= segmentValueThreshold || (percentage > i * 10 && percentage >= (i * 10 + 5)));
                const strokeColor = isSegmentLit ? activeColor : trackColor;
                svgHtml += `<path d="M ${x1} ${y1} A ${r} ${r} 0 0 1 ${x2} ${y2}" fill="none" stroke="${strokeColor}" stroke-width="5" stroke-linecap="round"/>`;
            }
            svgHtml += `</svg>`;

            const lang = localStorage.getItem('nbl9_v6_lang') || 'en';
            let labelVal = percentage.toString();
            if (lang === 'ne') {
                const nums = ["०", "१", "२", "३", "४", "५", "६", "७", "८", "९"];
                labelVal = percentage.toString().split('').map(digit => nums[digit] || digit).join('');
            }

            return `
                <div class="circle-container" style="width: ${size}px; height: ${size}px; position: relative;">
                    ${svgHtml}
                    <div class="circle-center-label" style="color: ${percentage > 0 ? activeColor : 'var(--text-secondary)'}; font-size: ${size * 0.22}px;">${labelVal}%</div>
                </div>
            `;
        }

