        function calculateMetricsDashboard() {
            const lang = localStorage.getItem('nbl9_v6_lang') || 'en';
            let totalGlobal = 0, doneGlobal = 0;
            let p1ka_total = 0, p1ka_done = 0;
            let p1kb_total = 0, p1kb_done = 0;
            let p2ka_total = 0, p2ka_done = 0;
            let p2kb_total = 0, p2kb_done = 0;

            chronologicalPlan.forEach(day => {
                day.items.forEach(it => {
                    totalGlobal++;
                    const isDone = localStorage.getItem(`${it.id}_done`) === 'true';
                    if (isDone) doneGlobal++;

                    const code = it.en.customNumber;
                    if (code.startsWith("I.A")) {
                        p1ka_total++; if (isDone) p1ka_done++;
                    } else if (code.startsWith("I.B")) {
                        p1kb_total++; if (isDone) p1kb_done++;
                    } else if (code.startsWith("II.A")) {
                        p2ka_total++; if (isDone) p2ka_done++;
                    } else if (code.startsWith("II.B")) {
                        p2kb_total++; if (isDone) p2kb_done++;
                    }
                });
            });

            const pctGlobal = totalGlobal > 0 ? Math.round((doneGlobal / totalGlobal) * 100) : 0;
            const pctP1ka = p1ka_total > 0 ? Math.round((p1ka_done / p1ka_total) * 100) : 0;
            const pctP1kb = p1kb_total > 0 ? Math.round((p1kb_done / p1kb_total) * 100) : 0;
            const pctP2ka = p2ka_total > 0 ? Math.round((p2ka_done / p2ka_total) * 100) : 0;
            const pctP2kb = p2kb_total > 0 ? Math.round((p2kb_done / p2kb_total) * 100) : 0;

            document.getElementById('header-mini-circle-slot').innerHTML = generateSegmentedCircleHTML(pctGlobal, 52);

            const convertTextCount = (done, total) => {
                let text = `${done} / ${total}`;
                if (lang === 'ne') {
                    const nums = ["०", "१", "२", "३", "४", "५", "६", "७", "८", "९"];
                    text = text.split('').map(digit => nums[digit] || digit).join('');
                }
                return `${text} ${uiLexicon[lang].doneSuffix}`;
            };

            document.getElementById('circle-slot-p1ka').innerHTML = generateSegmentedCircleHTML(pctP1ka, 100);
            document.getElementById('sub-lbl-p1ka').innerText = convertTextCount(p1ka_done, p1ka_total);

            document.getElementById('circle-slot-p1kb').innerHTML = generateSegmentedCircleHTML(pctP1kb, 100);
            document.getElementById('sub-lbl-p1kb').innerText = convertTextCount(p1kb_done, p1kb_total);

            document.getElementById('circle-slot-p2ka').innerHTML = generateSegmentedCircleHTML(pctP2ka, 100);
            document.getElementById('sub-lbl-p2ka').innerText = convertTextCount(p2ka_done, p2ka_total);

            document.getElementById('circle-slot-p2kb').innerHTML = generateSegmentedCircleHTML(pctP2kb, 100);
            document.getElementById('sub-lbl-p2kb').innerText = convertTextCount(p2kb_done, p2kb_total);
        }

