        function processNoteDocumentUpload(event, inputElement) {
            const file = event.target.files[0];
            if (!file) return;

            const parentBlock = inputElement.closest('[id^="sess_block_"]') || inputElement.closest('.day-card') || inputElement.closest('.day-body');
            if (!parentBlock) {
                alert("Error: Note-pad container context not found.");
                return;
            }

            const noteEditor = parentBlock.querySelector('[contenteditable="true"]') || parentBlock.querySelector('textarea');
            const statusIndicator = inputElement.parentElement.querySelector('.upload-status-indicator');

            if (!noteEditor) {
                alert("Active note editor pane not found in this subsection.");
                return;
            }

            // Extract the itemId from the editor's id attribute (e.g., "editor_item_1" → "item_1")
            const editorItemId = noteEditor.id ? noteEditor.id.replace('editor_', '') : null;

            if (statusIndicator) statusIndicator.textContent = "Processing file...";

            if (file.name.endsWith('.pptx') || file.name.endsWith('.ppt')) {
                if (statusIndicator) statusIndicator.textContent = "";

                const confirmConvert = confirm("For pixel-perfect slide layout rendering within your offline-capable Notes Pad, please save your PowerPoint slides as a PDF file and upload it. Would you like instructions on how to embed it directly?");
                if (confirmConvert) {
                    const formatGuidance = document.createElement('div');
                    formatGuidance.style.cssText = "background: rgba(245, 158, 11, 0.1); border-left: 4px solid var(--warning); padding: 10px; margin: 8px 0; border-radius: 4px; font-size: 12px; color: var(--text-primary);";
                    formatGuidance.innerHTML = `<strong>💡 Presentation Insertion Guide:</strong> Save presentation via <em>File > Save As / Export to PDF</em> in PowerPoint, then drop the PDF here to automatically extract and format each slide page as a high-resolution embedded note image.`;

                    if (noteEditor.tagName === 'TEXTAREA') {
                        noteEditor.value += `\n[System Notice: Export PPTX to PDF to render slides as high-res images here.]\n`;
                    } else {
                        noteEditor.appendChild(formatGuidance);
                    }
                    // Save after PPTX guidance is appended
                    if (editorItemId) {
                        saveModernNotesTextContent(editorItemId);
                    }
                }
                inputElement.value = '';
                return;
            }

            if (file.type === "application/pdf" || file.name.endsWith('.pdf')) {
                const reader = new FileReader();
                reader.onload = function () {
                    const typedarray = new Uint8Array(this.result);

                    window.pdfjsLib.getDocument(typedarray).promise.then(async function (pdf) {
                        const totalPages = pdf.numPages;
                        if (statusIndicator) statusIndicator.textContent = `Extracting ${totalPages} slide pages...`;

                        const slideBundleContainer = document.createElement('div');
                        slideBundleContainer.className = "embedded-slide-deck";
                        slideBundleContainer.setAttribute('data-source-file', file.name);
                        slideBundleContainer.setAttribute('data-total-pages', totalPages);
                        slideBundleContainer.style.cssText = "display: flex; flex-direction: column; gap: 12px; margin: 10px 0; width: 100%; position: relative; padding-top: 30px; background: rgba(0,0,0,0.02); border: 1px dashed var(--border); border-radius: 8px;";
                        
                        slideBundleContainer.innerHTML = `<button type="button" class="btn-delete-pdf" onclick="deleteEmbeddedPDF(this)" style="position: absolute; top: 5px; right: 5px; background: #ef4444; color: white; border: none; border-radius: 4px; padding: 4px 8px; cursor: pointer; font-size: 12px; font-weight: bold; z-index: 10;">🗑️ Delete PDF</button>`;

                        for (let pageNum = 1; pageNum <= totalPages; pageNum++) {
                            try {
                                if (statusIndicator) statusIndicator.textContent = `Rendering page ${pageNum} of ${totalPages}...`;

                                const page = await pdf.getPage(pageNum);
                                const scale = 1.5;
                                const viewport = page.getViewport({ scale: scale });

                                const canvas = document.createElement('canvas');
                                const context = canvas.getContext('2d');
                                canvas.height = viewport.height;
                                canvas.width = viewport.width;

                                const renderContext = {
                                    canvasContext: context,
                                    viewport: viewport
                                };

                                await page.render(renderContext).promise;

                                // Use JPEG with 0.7 quality instead of PNG to reduce base64 size by ~80%
                                // This is critical for localStorage persistence (5MB limit per origin)
                                const imgUrl = canvas.toDataURL('image/jpeg', 0.7);
                                const img = document.createElement('img');
                                img.src = imgUrl;
                                img.className = "rendered-note-slide-page";
                                img.alt = `${file.name} - Page ${pageNum}`;
                                img.style.cssText = "width: 100%; max-width: 100%; height: auto; border-radius: 6px; box-shadow: 0 2px 8px rgba(0,0,0,0.12); border: 1px solid var(--border); display: block;";

                                slideBundleContainer.appendChild(img);
                            } catch (pageError) {
                                console.error(`Failed rendering slide page ${pageNum}:`, pageError);
                            }
                        }

                        if (noteEditor.tagName === 'TEXTAREA') {
                            noteEditor.value += `\n[Embedded Document: ${file.name} - ${totalPages} Pages processed]\n`;
                            alert("Document successfully linked. Note: Image rendering requires a Rich Text contenteditable block. Text tag appended.");
                        } else {
                            noteEditor.appendChild(slideBundleContainer);

                            // Explicitly save the notes content including the embedded images to IDB
                            if (editorItemId) {
                                try {
                                    await saveModernNotesTextContent(editorItemId);
                                    if (statusIndicator) statusIndicator.textContent = `✓ ${totalPages} pages embedded & saved securely`;
                                } catch (saveError) {
                                    console.error('Save error:', saveError);
                                    if (statusIndicator) statusIndicator.textContent = "⚠️ Rendered but save failed";
                                }
                            } else {
                                if (statusIndicator) statusIndicator.textContent = "✓ Document slides embedded (manual save needed)";
                            }
                        }

                        setTimeout(() => { if (statusIndicator) statusIndicator.textContent = ""; }, 6000);

                    }).catch(function (error) {
                        console.error("PDF Parsing error:", error);
                        if (statusIndicator) statusIndicator.textContent = "❌ Processing error";
                    });
                };

                reader.readAsArrayBuffer(file);
            }

            inputElement.value = '';
        }