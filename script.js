        const canvasBrushes = {};

        function initCanvasDrawingEngine(itemId) {
            const canvas = document.getElementById(`canvas_${itemId}`);
            const wrapper = document.getElementById(`work_wrap_${itemId}`);
            if (!canvas || !wrapper) return;

            const ctx = canvas.getContext('2d');
            canvasBrushes[itemId] = { color: '#ef4444', isDrawing: false };

            resizeCanvasNode(itemId);

            idbGet(`${itemId}_canvas`).then(idbSketch => {
                const existingSketch = idbSketch || localStorage.getItem(`${itemId}_canvas`);
                if (existingSketch) {
                    const img = new Image();
                    img.onload = () => { ctx.drawImage(img, 0, 0); };
                    img.src = existingSketch;
                }
            });

            function getCanvasCoordinates(e) {
                const rect = canvas.getBoundingClientRect();
                const clientX = e.touches ? e.touches[0].clientX : e.clientX;
                const clientY = e.touches ? e.touches[0].clientY : e.clientY;
                return {
                    x: clientX - rect.left,
                    y: clientY - rect.top
                };
            }

            function startTrackingStroke(e) {
                if (!document.getElementById(`note_app_${itemId}`).classList.contains('sketch-mode-active')) return;
                e.preventDefault();
                canvasBrushes[itemId].isDrawing = true;
                const coord = getCanvasCoordinates(e);
                ctx.beginPath();
                ctx.moveTo(coord.x, coord.y);
            }

            function drawStrokeMovement(e) {
                if (!canvasBrushes[itemId].isDrawing) return;
                e.preventDefault();
                const coord = getCanvasCoordinates(e);
                ctx.lineTo(coord.x, coord.y);
                ctx.strokeStyle = canvasBrushes[itemId].color;
                ctx.lineWidth = 3;
                ctx.lineCap = 'round';
                ctx.lineJoin = 'round';
                ctx.stroke();
            }

            function freezeStrokeTracking() {
                if (canvasBrushes[itemId].isDrawing) {
                    canvasBrushes[itemId].isDrawing = false;
                    const dataUrl = canvas.toDataURL();
                    try { localStorage.setItem(`${itemId}_canvas`, dataUrl); } catch (e) { }
                    idbSet(`${itemId}_canvas`, dataUrl);
                }
            }

            canvas.addEventListener('mousedown', startTrackingStroke);
            canvas.addEventListener('mousemove', drawStrokeMovement);
            canvas.addEventListener('mouseup', freezeStrokeTracking);
            canvas.addEventListener('mouseleave', freezeStrokeTracking);

            canvas.addEventListener('touchstart', startTrackingStroke, { passive: false });
            canvas.addEventListener('touchmove', drawStrokeMovement, { passive: false });
            canvas.addEventListener('touchend', freezeStrokeTracking);
        }

